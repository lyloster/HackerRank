int getNode(SinglyLinkedListNode* head, int positionFromTail) {
    //reverse list
    SinglyLinkedListNode* next = NULL, *prev = NULL, *current = head;

    while(current != NULL){
        next = current->next;
        current->next = prev;
        prev = current;
        current = next;
    }
    head = prev;

    //start at the end
    current = head; 
    for(int i = 0; i < positionFromTail; i++){
        current = current->next;
    }

    int a = current->data;
    return a;

}

